package pineapple.iq_quest.data;


public class InfoClass {
	public int _id;
	public String name;
	public String old;
	public String email;
	
	public InfoClass(){}
	
	public InfoClass(int _id , String name , String old , String email){
		this._id = _id;
		this.name = name;
		this.old = old;
		this.email = email;
	}
	
}

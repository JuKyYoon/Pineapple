package pineapple.iq_quest.data;

public class InfoClass {
	public int _id;
	public String name;
	public String contact;
	public String email;
	
	public InfoClass(){}
	
	public InfoClass(int _id , String name , String contact , String email){
		this._id = _id;
		this.name = name;
		this.contact = contact;
		this.email = email;
	}
	
}
